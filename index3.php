                <br><p>Edytor wideo</p><br>
                <span>Edytor wideo (wideoedytor) – program komputerowy do montażu cyfrowych filmów.<br>
   Współcześnie podstawowym materiałem są filmy wideo rejestrowane przez cyfrową kamerę wideo, fotografie rejestrowane za pomocą cyfrowych aparatów fotograficznych, oraz nagrania dźwiękowe.
       Proste w obsłudze edytory wideo, podobnie jak edytory grafiki, stały się popularne wśród domowych użytkowników na skutek gwałtownego rozwoju rynku cyfrowej fotografii i cyfrowego wideo.</span>
                <br><p>Etapy pracy</p><br>
                <span>Praca z edytorem wideo obejmuje cztery etapy:
   <ul>
       <li>	pobieranie materiału ze źródeł zewnętrznych.</li>
       <li>	układanie materiału na odpowiednich ścieżkach - wideo, obrazu i dźwięku.</li>
       <li>	edycja ścieżek (cięcie, sklejanie, dodawanie napisów, efektów specjalnych, przejść między fragmentami filmu lub fotografiami, głosowych komentarzy).</li>
       <li>	kompilacja filmu – jako pojedynczego pliku filmowego (w formatach avi, mpg, mov, flv, wmv, mswmm) lub jako całej prezentacji na płycie CD/DVD (DVD authoring), a także na płycie Blu-ray, kasecie wideo.</li>
       Ostatni etap jest często wykonywany w wyspecjalizowanych programach, niemniej większość czołowych edytorów wideo posiada wbudowane, uproszczone narzędzia do tworzenia interfejsu płyt CD, DVD, Blu-ray.</span>
                <br><p>Aplikacje</p><br>
                <span>Najbardziej uznanym edytorem wideo przeznaczonym do zastosowań profesjonalnych jest Avid Media Composer. Produkty innych firm np. SpeedEDIT firmy NewTek cieszą się mniejszą popularnością wśród profesjonalistów.
   Inne programy przeznaczone dla zastosowań półprofesjonalnych i profesjonalnych: Adobe Premiere Pro, Avid Liquid Edition, Canopus Edius, Cinelerra, Final Cut Pro, Sony Vegas.
   Najbardziej znane edytory wideo dla domowego użytkownika to: Adobe Premiere Elements (Adobe), PowerDirector Pro (Cyberlink), Magix video deLuxe (Magix), Pinnacle Studio (Pinnacle), VideoWave (MGI Software), VideoImpression (Arcsoft), VideoStudio (Ulead), iMovie (Apple), Windows Movie Maker (Microsoft), WinDVD Creator (InterVideo), MoviePlus (Serif).
       W polskich wersjach językowych dostępne są polski Koala Film Studio 2 DV oraz zachodniej produkcji Pinnacle Studio, iMovie, Windows Movie Maker, WinDVD Creator oraz Corel VideoStudio Pro. </span>
